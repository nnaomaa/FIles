// escape characters in js
// to display apostrophe 's
console.log("It\'s raining");
// to display double quotes
console.log("Hello \"Coders!\"");
//to display gap of five spaces
console.log("Hello \tCoders!");
//to display backslash
console.log("Hello \\Coders!");
//to display in new line
console.log("hello \nWorld");
//to use backspace - to remove previous character
console.log("Today is going to rain\b at 2:00pm");
//to demonstrate carriage return - move cursor to the line beginning
console.log("Hello hello\r");
console.log("\rWorld world"); 